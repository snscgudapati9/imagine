from flask import Flask, render_template, redirect, url_for, request, flash, send_from_directory
from flask_sqlalchemy import SQLAlchemy
from flask_login import LoginManager, UserMixin, login_user, login_required, logout_user, current_user
from werkzeug.utils import secure_filename
import os
from PIL import Image, ImageFilter, ImageOps

app = Flask(__name__)
app.config['SECRET_KEY'] = 'your_secret_key'
app.config['SQLALCHEMY_DATABASE_URI'] = 'sqlite:///users.db'
app.config['UPLOAD_FOLDER'] = 'static/images'
db = SQLAlchemy(app)
login_manager = LoginManager()
login_manager.init_app(app)

class User(db.Model, UserMixin):
    id = db.Column(db.Integer, primary_key=True)
    username = db.Column(db.String(150), nullable=False, unique=True)
    password = db.Column(db.String(150), nullable=False)

@login_manager.user_loader
def load_user(user_id):
    return User.query.get(int(user_id))

@app.route('/')
@login_required
def home():
    return render_template('index.html')

# This is where you add the /upload route handler
@app.route('/upload', methods=['GET', 'POST'])
@login_required
def upload():
    if request.method == 'POST':
        file = request.files['file']
        if file:
            filename = secure_filename(file.filename)
            filepath = os.path.join(app.config['UPLOAD_FOLDER'], filename)
            file.save(filepath)

            # Apply cartoon effect (example logic)
            img = Image.open(filepath)
            cartoon_image = apply_cartoon_effect(img)
            cartoon_image_path = os.path.join(app.config['UPLOAD_FOLDER'], 'cartoon_' + filename)
            cartoon_image.save(cartoon_image_path)

            flash('Image uploaded and converted successfully!')
            return render_template('display.html', filename='cartoon_' + filename)
    return render_template('upload.html')

def apply_cartoon_effect(image):
    # Apply basic cartoon-like effect (example logic)
    edges = image.convert('L').filter(ImageFilter.FIND_EDGES)
    inverted_edges = ImageOps.invert(edges)
    blurred = inverted_edges.filter(ImageFilter.GaussianBlur(1))
    cartoon_image = Image.blend(image, blurred.convert('RGB'), alpha=0.5)
    return cartoon_image

# Route to display the cartoon image
@app.route('/static/images/<filename>')
def display_image(filename):
    return send_from_directory(app.config['UPLOAD_FOLDER'], filename)

@app.route('/login', methods=['GET', 'POST'])
def login():
    if request.method == 'POST':
        username = request.form['username']
        password = request.form['password']
        user = User.query.filter_by(username=username, password=password).first()
        if user:
            login_user(user)
            return redirect(url_for('home'))
        flash('Login Unsuccessful. Please check username and password.')
    return render_template('login.html')

@app.route('/register', methods=['GET', 'POST'])
def register():
    if request.method == 'POST':
        username = request.form['username']
        password = request.form['password']
        new_user = User(username=username, password=password)
        db.create_all()  # Create the database
        db.session.add(new_user)
        db.session.commit()
        flash('Registration successful! You can now log in.')
        return redirect(url_for('login'))
    return render_template('register.html')

@app.route('/logout')
@login_required
def logout():
    logout_user()
    return redirect(url_for('login'))

if __name__ == '__main__':
    app.run(debug=True)